// ============================================
// JavaScript Mathematical Operation Shorthand
// ============================================

// 1️⃣ Addition Shorthand (+=)
let x = 10;
x += 5;  // equivalent to x = x + 5
console.log("Addition shorthand (x += 5):", x); // 15

// 2️⃣ Subtraction Shorthand (-=)
let y = 20;
y -= 4;  // equivalent to y = y - 4
console.log("Subtraction shorthand (y -= 4):", y); // 16

// 3️⃣ Multiplication Shorthand (*=)
let a = 5;
a *= 3;  // equivalent to a = a * 3
console.log("Multiplication shorthand (a *= 3):", a); // 15

// 4️⃣ Division Shorthand (/=)
let b = 50;
b /= 5;  // equivalent to b = b / 5
console.log("Division shorthand (b /= 5):", b); // 10

// 5️⃣ Modulus Shorthand (%=)
let c = 10;
c %= 3;  // equivalent to c = c % 3
console.log("Modulus shorthand (c %= 3):", c); // 1

// 6️⃣ Increment (++) / Decrement (--)
let i = 5;
i++; // equivalent to i = i + 1
console.log("Increment (i++):", i); // 6

let j = 5;
j--; // equivalent to j = j - 1
console.log("Decrement (j--):", j); // 4

// 7️⃣ Exponent Shorthand (**=)
let base = 2;
base **= 3; // equivalent to base = base ** 3
console.log("Exponent shorthand (base **= 3):", base); // 8

// 8️⃣ Combined Example
let num = 10;
num += 5;   // 15
num *= 2;   // 30
num -= 10;  // 20
num /= 2;   // 10
num %= 3;   // 1
num **= 4;  // 1^4 = 1
console.log("Combined shorthand result:", num);
