// =====================================
// JavaScript Variable Naming Convention
// =====================================

// ✅ 1. camelCase (JavaScript Standard)
let userName = "Sohel";
let totalPrice = 1500;
let productCount = 3;

// -------------------------------------

// ✅ 2. Meaningful & Descriptive Names
let studentAge = 20;
let courseName = "JavaScript Basics";
let countryName = "Bangladesh";

// -------------------------------------

// ✅ 3. Number (Numeric) Variable Naming
let price = 500;
let totalMarks = 420;
let maxLimit = 100;

// -------------------------------------

// ✅ 4. String Variable Naming
let firstName = "Rahim";
let lastName = "Uddin";
let fullName = firstName + " " + lastName;

// -------------------------------------

// ✅ 5. Boolean Variable Naming
// Boolean সবসময় is / has / can দিয়ে শুরু করা ভালো
let isLoggedIn = true;
let hasPermission = false;
let canEditProfile = true;

// -------------------------------------

// ✅ 6. Constant Naming (Fixed Value)
const PI = 3.1416;
const MAX_USERS = 100;

// -------------------------------------

// ❌ 7. Bad Naming Examples (Avoid These)
// (শুধু বোঝার জন্য, ব্যবহার করো না)

// let x = 10;               // unclear
// let a = "Rahim";           // meaningless
// let user name = "Ali";    // space not allowed
// let 1stUser = "Hasan";    // cannot start with number
// let UserName = "Ali";     // PascalCase (avoid in JS)
// let isloggedin = true;    // unclear boolean name

// -------------------------------------

